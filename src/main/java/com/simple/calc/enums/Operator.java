package com.simple.calc.enums;

/**
 * Перечисление Operator определяет различные операторы, которые могут использоваться в калькуляторе.
 * - DEFAULT: оператор по умолчанию
 * - ADD: оператор сложения
 * - MULTIPLY: оператор умножения
 * - SPECIAL_MULTIPLY: особый оператор умножения
 * - POW: оператор возведения в степень
 */
public enum Operator {
    DEFAULT, ADD, MULTIPLY, SPECIAL_MULTIPLY, POW
}